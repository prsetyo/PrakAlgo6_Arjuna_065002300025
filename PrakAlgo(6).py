import math

def luas_kubus(sisi):
    return 6 * sisi ** 2

def luas_balok(panjang, lebar, tinggi):
    return 2 * (panjang * lebar + panjang * tinggi + lebar * tinggi)

def luas_tabung(jari_jari, tinggi):
    luas_alas = math.pi * jari_jari ** 2
    luas_permukaan = 2 * math.pi * jari_jari * (jari_jari + tinggi)
    return luas_alas + luas_permukaan

def luas_kerucut(jari_jari, tinggi):
    s = math.sqrt(jari_jari ** 2 + tinggi ** 2)
    luas_alas = math.pi * jari_jari ** 2
    luas_permukaan = math.pi * jari_jari * s
    return luas_alas + luas_permukaan

def luas_bola(jari_jari):
    return 4 * math.pi * jari_jari ** 2

